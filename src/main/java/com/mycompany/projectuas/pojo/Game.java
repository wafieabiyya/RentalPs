public class Game {
    private String ID;
    private String namaGame;

    public Game() {
    }

    public Game(String ID, String namaGame) {
        this.ID = ID;
        this.namaGame = namaGame;
    }

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getNamaGame() {
        return namaGame;
    }

    public void setNamaGame(String namaGame) {
        this.namaGame = namaGame;
    }

    
    
}
